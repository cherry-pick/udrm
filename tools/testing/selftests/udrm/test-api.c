/* make sure /dev/udrm exists, is a cdev and accessible */
static void test_api_cdev(void)
{
	struct stat st;
	int r, fd;

	r = access(test_path, F_OK);
	assert(r >= 0);

	r = stat(test_path, &st);
	assert(r >= 0);
	assert((st.st_mode & S_IFMT) == S_IFCHR);

	fd = open(test_path, O_RDWR | O_CLOEXEC | O_NONBLOCK | O_NOCTTY);
	assert(fd >= 0);

	close(fd);
}

/* make sure simple REGISTER/UNREGISTER works */
static void test_api_registration(void)
{
	int r, fd;

	fd = open(test_path, O_RDWR | O_CLOEXEC | O_NONBLOCK | O_NOCTTY);
	assert(fd >= 0);

	r = ioctl(fd, UDRM_CMD_UNREGISTER, NULL);
	assert(r < 0 && errno == ENOTCONN);

	r = ioctl(fd, UDRM_CMD_REGISTER, NULL);
	assert(r >= 0);

	r = ioctl(fd, UDRM_CMD_REGISTER, NULL);
	assert(r < 0 && errno == EISCONN);

	r = ioctl(fd, UDRM_CMD_UNREGISTER, NULL);
	assert(r >= 0);

	r = ioctl(fd, UDRM_CMD_UNREGISTER, NULL);
	assert(r < 0 && errno == ESHUTDOWN);

	r = ioctl(fd, UDRM_CMD_REGISTER, NULL);
	assert(r < 0 && errno == ESHUTDOWN);

	close(fd);
}

/* make sure simple PLUG/UNPLUG works */
static void test_api_plugging(void)
{
	struct udrm_cmd_plug plug = {};
	int r, fd;

	fd = open(test_path, O_RDWR | O_CLOEXEC | O_NONBLOCK | O_NOCTTY);
	assert(fd >= 0);

	r = ioctl(fd, UDRM_CMD_PLUG, &plug);
	assert(r < 0 && errno == ENOTCONN);

	r = ioctl(fd, UDRM_CMD_REGISTER, NULL);
	assert(r >= 0);

	r = ioctl(fd, UDRM_CMD_PLUG, &plug);
	assert(r >= 0);

	r = ioctl(fd, UDRM_CMD_UNPLUG, NULL);
	assert(r >= 0);

	r = ioctl(fd, UDRM_CMD_PLUG, &plug);
	assert(r >= 0);

	plug.ptr_edid = (uintptr_t)valid_edid;
	plug.n_edid = sizeof(valid_edid);
	r = ioctl(fd, UDRM_CMD_PLUG, &plug);
	assert(r >= 0);

	plug.ptr_edid = (uintptr_t)invalid_edid;
	plug.n_edid = sizeof(invalid_edid);
	r = ioctl(fd, UDRM_CMD_PLUG, &plug);
	assert(r < 0 && errno == EINVAL);

	close(fd);
}

int test_api(void)
{
	test_api_cdev();
	test_api_registration();
	test_api_plugging();

	return TEST_OK;
}
